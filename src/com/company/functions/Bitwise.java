package functions;

public class Bitwise {
    public static void main(String[] args) {
        System.out.println(2 & 3);
        System.out.println(2 | 3);
        System.out.println(2 ^ 3);
        System.out.println(~3);
        System.out.println(34 <<2);
        System.out.println(34 >> 2);
        System.out.println(-4 << 2);
        System.out.println(-4 >> 2);
    }
    
}
